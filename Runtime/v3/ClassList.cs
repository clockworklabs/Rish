            private int _index;
            private string _current;

            public Enumerator(ClassList list)
            {
                _list = list;
                _index = 0;
                _current = default;
            }

            public string Current => _current;

            public bool MoveNext()
            {
                if (_index >= _list.Count)
                {
                    return false;
                }
                
                _current = _list[_index++];
                
                return true;
            }
